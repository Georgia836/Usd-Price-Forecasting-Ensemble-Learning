import yfinance as yf
import pandas as pd
import numpy as np
import plotly.express as px
import matplotlib.pyplot as plt

from sklearn.linear_model import LinearRegression
from sklearn.ensemble import RandomForestRegressor, BaggingRegressor, StackingRegressor
from sklearn.tree import DecisionTreeRegressor
from sklearn.metrics import mean_absolute_error

"""## 2. Download USD Data

The dataset is downloaded using the `yfinance` library. The selected ticker is `EURUSD=X`, which represents the EUR/USD exchange rate. The available daily OHLC values are kept, while unnecessary columns are removed.
"""

ticker = "EURUSD=X"

df = yf.download(ticker, period="5y", interval="1d")
df = df.reset_index()

if isinstance(df.columns, pd.MultiIndex):
    df.columns = df.columns.get_level_values(0)

columns_to_drop = [col for col in ["Adj Close", "Volume", "Dividends", "Stock Splits"] if col in df.columns]
df = df.drop(columns=columns_to_drop)

df = df.dropna()
df.head()

print("Dataset shape:", df.shape)
display(df.describe())
display(df.info())

"""## 3. Line Plot of Open, High, Low and Close

The line plot shows the daily values of Open, High, Low and Close over time. Plotly is used because it provides interactive zooming and easier visual inspection of time-series behavior.
"""

fig = px.line(
    df,
    x="Date",
    y=["Open", "High", "Low", "Close"],
    title="EUR/USD Exchange Rate - Open, High, Low and Close Values"
)

fig.update_layout(
    width=1200,
    height=650,
    xaxis_title="Date",
    yaxis_title="Exchange Rate",
    legend_title="Price Type"
)

fig.show()

"""## 4. Stationarity Discussion

From the line plot, the time series does not appear to be strictly stationary. The values change over time and show periods with different levels, trends, and volatility.

A stationary time series should have a relatively stable mean and variance over time. In this case, the exchange rate moves through different regimes, meaning that the distribution of values is not constant across the full period.

## 5. Problems of Training on Non-Stationary Time Series

Training a regressor directly on non-stationary time-series data can cause several problems. A model may learn patterns from one period that do not hold in a later period. This is especially important in financial data, where market conditions change over time.

In this dataset, the exchange rate changes across the five-year period, so a model trained on earlier values may not generalize perfectly to future values. This can lead to unstable predictions and poor test performance.

## 6. Can We Predict Tomorrow's Close from Recent OHLC Values?

It is possible to attempt predicting the next-day Close value using today's and previous Open, High, Low and Close values. In other words, we try to approximate a function that maps recent OHLC information to the next Close value.

However, financial time series are noisy and influenced by many external factors, such as macroeconomic news, interest rates, market expectations and geopolitical events. Therefore, the prediction is possible as a machine learning task, but it is expected to be difficult and uncertain.

## 7. Why Random Train-Test Split Is Not Appropriate

A random train-test split is not appropriate for time-series prediction because it would mix past and future observations. This creates data leakage, since the model may train on future examples and then be evaluated on past examples.

For time-series data, the chronological order must be preserved. Therefore, the model should be trained on earlier data and tested on later data.

## 8. Train-Test Split by Date

The training set includes all observations before 2024, while the test set includes observations from 2024 onwards. This respects the chronological order of the time series.
"""

train_df = df[df["Date"] < "2024-01-01"].copy()
test_df = df[df["Date"] >= "2024-01-01"].copy()

print("Train period:", train_df["Date"].min(), "to", train_df["Date"].max())
print("Test period:", test_df["Date"].min(), "to", test_df["Date"].max())
print("Train shape:", train_df.shape)
print("Test shape:", test_df.shape)

train_features_df = train_df.drop(columns=["Date"])
test_features_df = test_df.drop(columns=["Date"])

train_features_df.head()

"""## 9. Sliding Window Function

The following function creates timeframes of length `N`. Each input contains `N` consecutive rows with the features Open, High, Low and Close. The target is the Close value of the next day.
"""

def create_timeframes(dataframe, N, target_col="Close"):
    values = dataframe.values
    target_values = dataframe[target_col].values
    X, y = [], []

    for i in range(len(dataframe) - N):
        X.append(values[i:i+N])
        y.append(target_values[i+N])

    return np.array(X), np.array(y)

"""## 10. Choosing the Timeframe Size N

For next-day prediction, a relatively small timeframe can be suitable because the target is very close in time. A small `N`, such as 5 trading days, captures recent short-term behavior without adding too much noise.

For a longer prediction horizon, such as one month ahead, a larger timeframe may be useful because the model would need more historical context. However, very large windows may also introduce irrelevant older information.

## 11. Create Train and Test Arrays with N = 5

The models used in this assignment require vector inputs. Therefore, each `N x 4` timeframe is reshaped into a one-dimensional vector of size `N * 4 = 20`.
"""

N = 5

x_train, y_train = create_timeframes(train_features_df, N)
x_test, y_test = create_timeframes(test_features_df, N)

x_train = x_train.reshape(x_train.shape[0], -1)
x_test = x_test.reshape(x_test.shape[0], -1)

print("x_train shape:", x_train.shape)
print("y_train shape:", y_train.shape)
print("x_test shape:", x_test.shape)
print("y_test shape:", y_test.shape)

print("First input of x_train:")
print(x_train[0])

"""## 12. Train Regression Models

Four regression models are trained and evaluated using Mean Absolute Error (MAE):

- Linear Regression
- Random Forest Regressor
- Bagging Regressor with Linear Regression estimator
- Stacking Regressor with Linear Regression and Decision Tree as base estimators
"""

models = {
    "Linear Regression": LinearRegression(),
    "Random Forest": RandomForestRegressor(random_state=0, n_estimators=100),
    "Bagging LR": BaggingRegressor(estimator=LinearRegression(), random_state=0, n_estimators=50),
    "Stacking LR+DT": StackingRegressor(
        estimators=[
            ("lr", LinearRegression()),
            ("dt", DecisionTreeRegressor(random_state=0))
        ],
        final_estimator=LinearRegression()
    )
}

results = []
predictions = {}

for name, model in models.items():
    model.fit(x_train, y_train)
    train_pred = model.predict(x_train)
    test_pred = model.predict(x_test)

    predictions[name] = test_pred

    results.append({
        "Model": name,
        "Train MAE": mean_absolute_error(y_train, train_pred),
        "Test MAE": mean_absolute_error(y_test, test_pred)
    })

results_df = pd.DataFrame(results).sort_values(by="Test MAE")
display(results_df)

plt.figure(figsize=(10, 5))

x_axis = np.arange(len(results_df["Model"]))
width = 0.35

plt.bar(x_axis - width/2, results_df["Train MAE"], width, label="Train MAE")
plt.bar(x_axis + width/2, results_df["Test MAE"], width, label="Test MAE")

plt.xticks(x_axis, results_df["Model"], rotation=30)
plt.xlabel("Regressor")
plt.ylabel("MAE")
plt.title("Train and Test MAE per Regressor")
plt.legend()
plt.show()

"""## 13. Brief Explanation of the Regressors

**Linear Regression:** Learns a linear relationship between the input features and the target value.

**Random Forest Regressor:** Combines many decision trees and averages their predictions to reduce overfitting and improve stability.

**Bagging Regressor:** Trains multiple versions of the same model on different bootstrap samples and averages their predictions.

**Stacking Regressor:** Combines multiple base models and trains a final model to learn how to best use their predictions.

# Improving the Models

## 14. Add Month and Day Features

Month and day are extracted from the Date column. These features may help the model because exchange rates can show seasonal patterns, monthly effects, or calendar-related behavior connected to economic announcements and market cycles.
"""

df_enhanced = df.copy()
df_enhanced["Month"] = df_enhanced["Date"].dt.month
df_enhanced["Day"] = df_enhanced["Date"].dt.day

df_enhanced.head()

"""## 15. Log Returns

Log returns transform absolute price values into relative changes. This transformation is commonly used in financial time series because it can help stabilize the variance and reduce the effect of non-stationarity.

For each column, the log return is calculated as:

$$r_t = \ln\left(rac{y_{t+1}}{y_t}
ight)$$
"""

log_df = df_enhanced.copy()
price_columns = [col for col in ["Open", "High", "Low", "Close"] if col in log_df.columns]

absolute_close_next = log_df["Close"].shift(-1)
current_close = log_df["Close"]
log_df["Close_Next_Absolute"] = absolute_close_next
log_df["Close_Current_Absolute"] = current_close

for col in price_columns:
    log_df[col] = np.log(log_df[col].shift(-1) / log_df[col])

log_df = log_df.dropna().reset_index(drop=True)
log_df.head()

for col in price_columns:
    plt.figure(figsize=(6, 4))
    plt.hist(log_df[col], bins=40)
    plt.title(f"Histogram of Log Returns - {col}")
    plt.xlabel(f"{col} Log Return")
    plt.ylabel("Frequency")
    plt.show()

"""## 16. Interpretation of a Predicted Log Return

If a model predicts `Ct+1 = 0.01`, this means that the predicted log return of the Close price is 0.01. This corresponds approximately to a 1% increase.

If today's Close price is `Ct = 0.95`, then the predicted absolute value for the next day is:

$$C_{t+1} = C_t \cdot e^{0.01}$$
"""

Ct = 0.95
predicted_log_return = 0.01
predicted_absolute_close = Ct * np.exp(predicted_log_return)

print("Predicted absolute Close:", predicted_absolute_close)

"""## 17. Repeat Preprocessing with Log Returns and Calendar Features

The same sliding-window approach is repeated using log returns and the additional calendar features Month and Day.
"""

log_train_df = log_df[log_df["Date"] < "2024-01-01"].copy()
log_test_df = log_df[log_df["Date"] >= "2024-01-01"].copy()

log_train_dates = log_train_df["Date"].reset_index(drop=True)
log_test_dates = log_test_df["Date"].reset_index(drop=True)

cols_to_drop = ["Date", "Close_Next_Absolute", "Close_Current_Absolute"]
log_train_features_df = log_train_df.drop(columns=cols_to_drop)
log_test_features_df = log_test_df.drop(columns=cols_to_drop)

print("Log train shape:", log_train_features_df.shape)
print("Log test shape:", log_test_features_df.shape)
log_train_features_df.head()

x_train_log, y_train_log = create_timeframes(log_train_features_df, N, target_col="Close")
x_test_log, y_test_log = create_timeframes(log_test_features_df, N, target_col="Close")

x_train_log = x_train_log.reshape(x_train_log.shape[0], -1)
x_test_log = x_test_log.reshape(x_test_log.shape[0], -1)

print("x_train_log shape:", x_train_log.shape)
print("y_train_log shape:", y_train_log.shape)
print("x_test_log shape:", x_test_log.shape)
print("y_test_log shape:", y_test_log.shape)

print("First input of x_train_log:")
print(x_train_log[0])

"""## 18. Train Models on Log Returns"""

models_log = {
    "Linear Regression": LinearRegression(),
    "Random Forest": RandomForestRegressor(random_state=0, n_estimators=100),
    "Bagging LR": BaggingRegressor(estimator=LinearRegression(), random_state=0, n_estimators=50),
    "Stacking LR+DT": StackingRegressor(
        estimators=[
            ("lr", LinearRegression()),
            ("dt", DecisionTreeRegressor(random_state=0))
        ],
        final_estimator=LinearRegression()
    )
}

results_log = []
predictions_log = {}

for name, model in models_log.items():
    model.fit(x_train_log, y_train_log)
    train_pred = model.predict(x_train_log)
    test_pred = model.predict(x_test_log)

    predictions_log[name] = test_pred

    results_log.append({
        "Model": name,
        "Train MAE": mean_absolute_error(y_train_log, train_pred),
        "Test MAE": mean_absolute_error(y_test_log, test_pred)
    })

results_log_df = pd.DataFrame(results_log).sort_values(by="Test MAE")
display(results_log_df)

plt.figure(figsize=(10, 5))

x_axis = np.arange(len(results_log_df["Model"]))
width = 0.35

plt.bar(x_axis - width/2, results_log_df["Train MAE"], width, label="Train MAE")
plt.bar(x_axis + width/2, results_log_df["Test MAE"], width, label="Test MAE")

plt.xticks(x_axis, results_log_df["Model"], rotation=30)
plt.xlabel("Regressor")
plt.ylabel("MAE")
plt.title("Train and Test MAE per Regressor after Log Returns")
plt.legend()
plt.show()

"""## 19. Convert Log-Return Predictions to Absolute Close Values

The best model from the log-return experiment is selected based on the lowest test MAE. Its predicted log returns are converted back to absolute Close prices using:

$$C_{t+1} = C_t \cdot e^{r_t}$$
"""

best_model_name = results_log_df.iloc[0]["Model"]
best_log_predictions = predictions_log[best_model_name]

actual_current_close_test = log_test_df["Close_Current_Absolute"].values[N:]
actual_next_close_test = log_test_df["Close_Next_Absolute"].values[N:]
predicted_next_close = actual_current_close_test * np.exp(best_log_predictions)

print("Best model:", best_model_name)
print("MAE in absolute Close values:", mean_absolute_error(actual_next_close_test, predicted_next_close))

"""## 20. Final Line Plot: Predicted vs Actual Close Values

The final plot compares the predicted absolute Close values of the best model with the real next-day Close values.
"""

plot_dates = log_test_df["Date"].values[N:]

prediction_plot_df = pd.DataFrame({
    "Date": plot_dates,
    "Actual Close": actual_next_close_test,
    "Predicted Close": predicted_next_close
})

fig = px.line(
    prediction_plot_df,
    x="Date",
    y=["Actual Close", "Predicted Close"],
    title=f"Actual vs Predicted Next-Day Close - {best_model_name}"
)

fig.update_layout(
    width=1200,
    height=650,
    xaxis_title="Date",
    yaxis_title="Close Value"
)

fig.show()

